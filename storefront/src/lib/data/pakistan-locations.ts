export function getCitiesByProvince(provinceCode: string): PakistanCity[] {
  return PAKISTAN_CITIES.filter((city) => city.provinceCode === provinceCode)
}

export function getProvinceByCode(code: string): PakistanProvince | undefined {
  return PAKISTAN_PROVINCES.find((p) => p.code === code)
}

export function getProvinceByName(name: string): PakistanProvince | undefined {
  return PAKISTAN_PROVINCES.find(
    (p) => p.name.toLowerCase() === name.toLowerCase()
  )
}
